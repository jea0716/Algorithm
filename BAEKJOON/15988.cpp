#include <iostream>

using namespace std;

long long int dp[1000001];

// 1 -> 1       1가지
// 2 -> 1+1, 2  2가지
// 3 -> 1+1+1, 1+2, 2+1, 3      4가지
// 4 -> 1+1+1+1, 1+1+2, 1+2+1, 1+3, 2+2, 2+1+1, 3+1     7가지
// 5 -> 1+1+1+1+1, 1+1+1+2, 1+1+2+1, 1+1+3, 1+2+2, 1+2+1+1, 1+3+1, 2+1+1+1, 2+1+2, 2+2+1, 2+3, 3+1+1, 3+2       13가지

int main(){
    int T, n; cin >> T;

    dp[0] = dp[1] = 1;
    dp[2] = 2;
    dp[3] = 4;

    for(int j=0; j<T; j++){
        cin >> n;
        for(int i=4; i<=n; i++){
            dp[i] = ((dp[i-1] % 1000000009) + (dp[i-2] % 1000000009) + (dp[i-3] % 1000000009))% 1000000009;
        }
        cout << dp[n] << endl;
    }

    return 0;
}