using namespace std;
//1 - 01 10 00 3
//2 - 0000 0001 0010 0100 1000 0110 1001 7
//3 - 000000 000001 000010 000100 001000 010000 100000
//    100100 100010 100001 011000 010010 010001 001001 000110
//    100110 011001 -17
//4 - 00000000 10010000 10001000 10000100 10000010 10000001 14
//    01100000 01001000 01000100 01000010 01000001 00100100 00100010 00100001
//    00011000 00010010 00010001 00001001 00000110
//    10011000 10010010 10010001 10001001 10000110 01100100 01100010 01100001
//    00100110 00011001 10011001 01100110
int dp[100001];

int main(){
    int n; cin >>n;
    dp[0] = 1;
    dp[1] = 3;
    dp[2] = 7;
    for(int i=2; i<=n; i++){
        dp[i] = (dp[i-1] * 2 + dp[i-2]) % 9901;
    }

    cout << dp[n] << endl;
    
    return 0;
}